// ! NOT logical operator
// typically used to reverse a condition's boolean value
// true - > false  false -> true

let temp = 15;
let sunny = false;
if(!(temp > 0)){
   console. log("lt's cold outside"); 
}
else{
    console. log("lt's warm outside");
}

if(!sunny){
   console.log("lt's cloudy outside"); 
}

else{
console. log("lt's sunny outside");
}